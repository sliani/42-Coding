char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (i == 0 || str[i - 1] < '0' || str[i - 1] > 'z'
		|| (str[i - 1] > '9' && str[i - 1] < 'A')
		|| (str[i - 1] > 'Z' && str[i - 1] < 'a'))
		{
			if (str[i] >= 'a' && str[i] <= 'z')
				str[i] = str[i] - 32;
		}
		else
		{
			if (str[i] >= 'A' && str[i] <= 'Z')
				str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}
