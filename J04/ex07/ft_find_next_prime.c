int		ft_find_next_prime(int nb)
{
	int i;

	i = 2;
	if (nb <= 1)
		nb = 2;
	else if (nb >= 3)
		while (nb > i)
		{
			if (nb % i == 0)
			{
				nb++;
				i = 1;
			}
			i++;
		}
	return (nb);
}
